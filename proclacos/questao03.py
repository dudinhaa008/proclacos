"""
3) Desenvolver um programa que apresente os quadrados dos números inteiros de 15 a 200.3) Desenvolver um programa que apresente os quadrados dos números inteiros de 15 a 200.
"""
numero = 15

while numero <= 200:
    quadrado = numero ** 2
    print(f"O quadrado de {numero} é {quadrado}")
    numero += 1